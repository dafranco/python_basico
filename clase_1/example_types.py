# Examples with different datatypes
text = "Hello, world!"
number = 3.14
integer = 3
is_valid = True
data_list = [1, 2, 3]
data_dict = {"name": "Alice", "age": 25}

print(type(integer))    # <class 'int'>
print(type(text))       # <class 'str'>
print(type(number))     # <class 'float'>
print(type(is_valid))   # <class 'bool'>
print(type(data_list))  # <class 'list'>
print(type(data_dict))  # <class 'dict'>